def generate_key():
    alph_list = list(rus_alp)
    random.shuffle(alph_list)
    key = "".join(alph_list)
    return key

def encrypt(text, key):
    # Создаем словарь замены
    zamena = {}
    for i, letter in enumerate(rus_alp):
        zamena[letter] = key[i]
    
    encrypt = []
    for char in text.lower():
        if char in zamena:
            encrypt.append(zamena[char])
        else:
            # Оставляем пробелы и знаки препинания как есть
            encrypt.append(char)
    
    return "".join(encrypt)

# Генерируем ключ и шифруем
key = generate_key()
encrypted_text = encrypt(text, key)

